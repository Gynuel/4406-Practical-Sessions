x <- c(11.201, 10.035, 11.118, 9.055, 9.434, 9.663, 10.403, 11.662, 9.285,8.84)
mean(x)
n<-length(x)
B<-1000
mx<-c(1:B)
for(i in 1:B){
  cat(i) # This prints the current value of 'i' to the console during each iteration (no space)
  boot.i<-sample(x,n,replace=T) # Gets sample with replacement since "replace = T"
  print(boot.i)
  mx[i]<-mean(boot.i)
}
var(mx)
hist(mx)


